public class Rectangle {

    private int length;
    private int width;
    private int xCoordinate;    //top left
    private int yCoordinate;    //top left

    public Rectangle(int len, int wid, int x, int y)
    {
        length = len;
        width = wid;
        xCoordinate = x;
        yCoordinate = y;
    }


}
